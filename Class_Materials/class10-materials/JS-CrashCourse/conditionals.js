//Conditionals

// if else else if 

const x = 6;
const y = 11;

if(x === 10){
    console.log('X is 10')
}else if(x>10) {
    console.log('x is greater than 10')
} else {
    console.log('X is not 10')
}

// || means OR in JS
if(x > 5 || y > 10){
    console.log('X is more than 5 or y is more than 10')
}

// && means AND in JS
if(x > 5 && y > 10){
    console.log('X is more than 5 and y is more than 10')
}